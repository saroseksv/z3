package sample;

public class Record {
    public Double c1;
    public Double c2;
    public Double c3;
    public Double c4;
    public Double c5;

    public Record(Double c1, Double c2, Double c3, Double c4, Double c5) {
        this.c1=c1;
        this.c2=c2;
        this.c3=c3;
        this.c4=c4;
        this.c5=c5;
    }

    public void setC1(Double c1) {
        this.c1 = c1;
    }
    public Double getC1() {
        return c1;
    }
    public void setC2(Double c2) {
        this.c2 = c2;
    }
    public Double getC2() {
        return c2;
    }
    public void setC3(Double c3) {
        this.c3 = c3;
    }
    public Double getC3() {
        return c3;
    }
    public void setC4(Double c4) {
        this.c4 = c4;
    }
    public Double getC4() {
        return c4;
    }
    public void setC5(Double c5) {
        this.c5 = c5;
    }
    public Double getC5() {
        return c5;
    }

}
